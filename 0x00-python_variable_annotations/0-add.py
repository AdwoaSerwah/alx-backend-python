#!/usr/bin/env python3
"""
Module that contains a function `add` with type annotations.
"""


def add(a: float, b: float) -> float:
    """
    Function that adds two float numbers and returns the result as a float.

    Args:
    a (float): The first number to add.
    b (float): The second number to add.

    Returns:
    float: The sum of the two input floats.
    """
    return a + b
