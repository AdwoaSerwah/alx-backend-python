#!/usr/bin/env python3
"""
Module that contains a function `floor` with type annotations.
"""

import math


def floor(n: float) -> int:
    """
    Function that returns the floor of a given float.

    Args:
    n (float): The float to be floored.

    Returns:
    int: The floored value of the float.
    """
    return math.floor(n)
